// ─── i18n ENGINE ─────────────────────────────────────────────
// Default: ID (Bahasa Indonesia)
let currentLang = localStorage.getItem('arbrion-lang') || 'id';

export function getLang() { return currentLang; }

export function t(key) {
  return translations[currentLang]?.[key]
    ?? translations['id']?.[key]
    ?? key;
}

export function tMarketers() {
  return translations[currentLang]?.marketers ?? translations['id'].marketers;
}

export function tDownloads() {
  return translations[currentLang]?.downloads ?? translations['id'].downloads;
}

export function setLang(lang) {
  if (lang !== 'id' && lang !== 'en') return;
  currentLang = lang;
  localStorage.setItem('arbrion-lang', lang);
  document.documentElement.lang = lang;
  updateAllText();
  updateLangButtons();
  document.dispatchEvent(new CustomEvent('lang:changed', { detail: { lang } }));
}

export function updateAllText() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n);
    if (!val || val === el.dataset.i18n) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else if (el.tagName === 'META') {
      el.content = val;
    } else {
      el.innerHTML = val;
    }
  });
}

export function updateLangButtons() {
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
    btn.setAttribute('aria-pressed', btn.dataset.lang === currentLang ? 'true' : 'false');
  });
}

export function waLink(number, msgKey, productName) {
  const num = number || t('wa_fab_number');
  let msg = t(msgKey || 'wa_msg_default');
  if (productName) msg = msg.replace('{product}', productName);
  return `https://wa.me/${num}?text=${encodeURIComponent(msg)}`;
}
